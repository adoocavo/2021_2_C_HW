#include<stdio.h>
#include<math.h>

int main()
{
	double pi = 3.14159;
	printf("지수함수 exp(1) = %f\n", exp(1.0));
	printf("로그함수 log(1) = %f\n", log(2.0));
	printf("승 함수 pow(2.0,2.3) = %f\n", pow(2.0,2.3));
	printf("제곱근 값  sqrt(16) = %f\n", sqrt(16.0));
	printf("절댓값 fabs(-pi) = %f\n", fabs(-pi));
	printf("작지 않은 최소 정수 값 ceil(pi) = %f\n", ceil(pi));
	printf("크지않은 최대 정수 값 floor(pi) = %f\n", floor(pi));
	printf("sin 값 sin(pi) = %f\n", sin(pi));
	printf("cosin 값 cos(pi) = %f\n", cos(pi));
	printf("tangent 값 tan(pi) = %f\n", tan(pi));
	
 } 
